# The while loop is to be used in situations where the number of iterations is unknown at first.
# The block of statements is executed in the while loop until the condition specified in the while loop is satisfied

# while loop executes the statement or group of statements repeatedly while the given condition is True.
# And when the condition becomes false, the loop ends and moves to the next statement after the loop

def function():
    count = 0
    while count < 5:
        print(count, end=' ')
        count = count + 1
    print()


function()


